package com.lc._2_add;

/**
 * Definition for singly-linked list.
 *
 * @author: Feng YuJie
 * @create: 2021/10/11 18:24
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
